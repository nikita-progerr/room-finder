@router.get("/groups", summary="Полный список групп")
async def get_all_groups():
    """
    Возвращает все группы, сгруппированные по направлениям.
    Синхронизировано с GROUP_CATALOG во фронтенд-компоненте SchedulePage.jsx.
    """
    # Собираем уникальные направления в порядке их первого появления
    dept_order: list[str] = []
    dept_map: dict[str, dict] = {}

    for group in GROUP_CATALOG:
        dept = group["dept"]
        if dept not in dept_map:
            dept_order.append(dept)
            dept_map[dept] = {
                "dept":   dept,
                "name":   group["name"],
                "groups": [],
            }
        dept_map[dept]["groups"].append({
            "id":     group["id"],
            "stream": group["stream"],
            "course": group["course"],
            "note":   group.get("note"),
        })

    return {
        "total":       len(GROUP_CATALOG),
        "departments": len(dept_map),
        "data":        [dept_map[d] for d in dept_order],
    }


@router.get("/groups/{dept}", summary="Группы одного направления")
async def get_groups_by_dept(dept: str):
    """
    Возвращает все группы указанного направления.

    Пример: GET /api/v1/schedule/groups/ИТ
    """
    dept_upper = dept.upper()
    groups = [
        {
            "id":     g["id"],
            "stream": g["stream"],
            "course": g["course"],
            "note":   g.get("note"),
        }
        for g in GROUP_CATALOG
        if g["dept"].upper() == dept_upper
    ]

    if not groups:
        raise HTTPException(
            status_code=404,
            detail=f"Направление «{dept}» не найдено. "
                   f"Доступные: {', '.join(sorted({g['dept'] for g in GROUP_CATALOG}))}",
        )

    return {
        "dept":   dept_upper,
        "name":   next(g["name"] for g in GROUP_CATALOG if g["dept"].upper() == dept_upper),
        "total":  len(groups),
        "groups": groups,
    }


